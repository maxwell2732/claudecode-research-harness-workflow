import sys
import logging
from pathlib import Path

PROJECT_ROOT = Path(__file__).resolve().parent.parent
LOG_DIR = PROJECT_ROOT / "logs"
LOG_DIR.mkdir(parents=True, exist_ok=True)

logging.basicConfig(
    level=logging.INFO,
    format="%(asctime)s %(levelname)s %(message)s",
    handlers=[
        logging.FileHandler(LOG_DIR / "00_scaffold.log", encoding="utf-8"),
        logging.StreamHandler(sys.stdout),
    ],
)
log = logging.getLogger(__name__)


def main():
    dirs = [
        PROJECT_ROOT / "data" / "intermediate",
        PROJECT_ROOT / "data" / "processed",
        PROJECT_ROOT / "scripts",
        PROJECT_ROOT / "logs",
        PROJECT_ROOT / "reports",
    ]
    for d in dirs:
        d.mkdir(parents=True, exist_ok=True)
        log.info("DIR OK: %s", d.relative_to(PROJECT_ROOT))

    merge_report = PROJECT_ROOT / "reports" / "merge_report.md"
    if not merge_report.exists():
        merge_report.write_text(
            "# CHNS Merge Report\n\n"
            "> Auto-generated by 00_scaffold.py. Each merge step appends a section.\n\n",
            encoding="utf-8",
        )
        log.info("CREATED reports/merge_report.md")
    else:
        log.info("EXISTS reports/merge_report.md")

    log.info("SUCCESS")


if __name__ == "__main__":
    try:
        main()
    except Exception as exc:
        logging.exception("FATAL: %s", exc)
        sys.exit(1)
